#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i] != '\0')
		i++;
	while (s[i] != c)
	{
		if (s[i] == s[0])
		{
			return (NULL);
		}
		i--;
	}
	return ((char*)&s[i]);
}
