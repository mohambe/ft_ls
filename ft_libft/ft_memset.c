#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	size_t	i;
	char	*s1;

	s1 = (char*)str;
	i = 0;
	while (i < n)
	{
		s1[i] = (unsigned)c;
		i++;
	}
	return (str);
}
